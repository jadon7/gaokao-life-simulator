function storySoFarText(history = [], year = 1) {
  if (!history.length) return "";
  const roles = appearedRoleNames(history).slice(0, 5).join("、");
  const life = lifeStatusHint(history, year);
  const relationship = describeTrack(history, "relationshipTrack", "");
  return `过去${history.length}年：${roles ? `已出场${roles}；` : ""}现实=${life || "继续推进"}；关系=${relationship || "继续推进"}`;
}

function timeFrameHint(year = 1, history = []) {
  const currentYear = Number(year || 1);
  const age = Math.min(35, 17 + currentYear);
  const educationState = educationStateHint(history);
  let stage = "大学早期";
  if (currentYear <= 3) stage = "大学早期";
  else if (currentYear === 4) stage = "大四分流";
  else if (currentYear === 5) stage = educationState === "继续读研" ? "读研/实习起步" : "毕业后一年";
  else if (currentYear === 6) stage = educationState === "继续读研" ? "读研/实习落地" : "毕业后两年";
  else if (currentYear <= 9) stage = educationState === "继续读研" ? "读研/实习" : "初入职场";
  else if (currentYear <= 12) stage = "城市平台";
  else if (currentYear <= 15) stage = "成年压力";
  else stage = "前辈收束";
  return `${age}岁左右，${stage}`;
}

function castIntroHint(storyCast = defaultStoryCast, history = []) {
  const text = JSON.stringify(history || []);
  const appeared = new Set(appearedRoleNames(history));
  const intros = [
    [storyCast.relationName, storyCast.relationIntro],
    [storyCast.roommateName, storyCast.roommateIntro],
    [storyCast.friendName, storyCast.friendIntro],
    [storyCast.externalName, storyCast.externalIntro],
    [storyCast.familyName, storyCast.familyIntro]
  ]
    .filter(([name, intro]) => name && intro && !appeared.has(name) && !text.includes(name))
    .map(([, intro]) => intro);
  return intros.length ? `可用身份短语：${intros.slice(0, 3).join("；")}` : "";
}

function topHollandCode(history = []) {
  const scores = { R: 0, I: 0, A: 0, S: 0, E: 0, C: 0 };
  history.forEach(item => {
    const holland = item?.holland || {};
    Object.keys(scores).forEach(key => {
      scores[key] += Number(holland[key] || 0);
    });
  });
  return Object.entries(scores).sort((a, b) => b[1] - a[1]).slice(0, 3).map(([key]) => key).join("") || "ICE";
}

function dominantTheme(history = []) {
  const texts = history.map(item => `${item?.sceneTitle || ""} ${item?.choiceTag || item?.choice || ""} ${item?.choiceText || ""}`).join(" ");
  if (/抢|冲|主动|公开|扩大|争取/.test(texts)) return "先推局面，再学会处理代价";
  if (/稳|守|确认|边界|止损|谨慎/.test(texts)) return "先守住底盘，再找更大的出手机会";
  if (/沟通|陪伴|一起|接住|安抚/.test(texts)) return "先把人接住，再把事情往前推";
  return "一路修正，一路把自己长出来";
}

export function getOutlineCard(year) {
  const card = outlineCards.find(item => Number(item.year) === Number(year));
  return card ? { ...card, phase: yearBeatForYear(card.year) } : null;
}

export function buildAnnualInput({ profile, history, year, totalGameYears = 18 }) {
  const storyCast = buildStoryCast(profile);
  const compactCast = compactStoryCastForYear(profile, storyCast, year);
  const outlineCard = compactOutlineCard(getOutlineCard(year), storyCast);
  const educationState = educationStateHint(history);
  const visibleEducationState = visibleEducationStateHint(history, year);
  const currentIncident = currentIncidentHint(profile, year, history, outlineCard);
  const majorAnchor = currentIncident || outlineCard?.mainTrack === "relationship" ? "" : majorAnchorHint(profile, year, history);
  const relationshipStage = relationshipStageHint(history, year);
  const recentCallbacks = getRecentCallbacks(history, 4)
    .filter(seed => educationState !== "已放弃考研" || !/考研|保研|复习|绩点/.test(seed));
  const stateHints = {
    recentCallbacks,
    recentSceneTitles: getRecentSceneTitles(history, 6),
    repeatGuard: repeatGuard(history),
    stageGuard: stageGuard(history),
    timeFrame: timeFrameHint(year, history),
    lifeStage: lifeStageHint(year, history),
    relationshipStage,
    relationshipStatus: describeTrack(history, "relationshipTrack", "暧昧升温：还在试探和靠近之间"),
    lifeStatus: lifeStatusHint(history, year)
  };
  if (majorAnchor) stateHints.majorAnchor = majorAnchor;
  const lastYear = lastYearText(history);
  const storySoFar = storySoFarText(history, year);
  if (lastYear) stateHints.lastYear = lastYear;
  if (storySoFar) stateHints.storySoFar = storySoFar;
  const recentIncidents = recentIncidentsHint(profile, history, year);
  const usedIncidents = usedIncidentsHint(profile, history);
  if (currentIncident) stateHints.currentIncident = currentIncident;
  if (recentIncidents) stateHints.recentIncidents = recentIncidents;
  if (usedIncidents) stateHints.usedIncidents = usedIncidents;
  if (Number(year) === 1) stateHints.openingFrame = buildOpeningFrame(profile);
  if (visibleEducationState) stateHints.educationState = visibleEducationState;
  if (Number(year || 1) <= 5) {
    const castIntroRule = castIntroHint(compactCast, history);
    if (castIntroRule) stateHints.castIntroRule = castIntroRule;
  }
  return {
    profile: compactProfile(profile),
    storyCast: compactCast,
    gameMeta: {
      totalYears: totalGameYears,
      currentYear: year
    },
    outlineCard,
    history: compactAnnualHistory(history),
    stateHints
  };
}

