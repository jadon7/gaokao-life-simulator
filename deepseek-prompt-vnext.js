function compactStoryCastForYear(profile = {}, existingCast = null, year = 1, history = [], relationshipStage = "", includeRelation = false) {
  const cast = compactStoryCast(profile, existingCast);
  const currentYear = Number(year || 1);
  const baseCast = {
    supportRoles: supportRolesForYear(currentYear, history),
    familyRoles: familyRolesForYear(currentYear),
    mentorIntro: cast.mentorIntro,
    familyIntro: cast.familyIntro
  };
  if (currentYear <= 5 || includeRelation) {
    baseCast.relationName = cast.relationName;
    baseCast.relationIntro = cast.relationIntro;
  }
  if (currentYear <= 3) {
    return {
      ...baseCast,
      campusRoles: ["同学", "室友", "专业课老师", "学长学姐"]
    };
  }
  if (currentYear <= 4) {
    return {
      ...baseCast,
      campusRoles: ["老师", "项目同伴", "行业前辈"]
    };
  }
  const historyText = JSON.stringify(history || []);
  const useSecondaryRelation = currentYear >= 9 && (
    relationshipStage === "新恋情萌芽" ||
    (cast.secondaryRelationName && historyText.includes(cast.secondaryRelationName))
  );
  const matureCast = { ...baseCast };
  if (includeRelation || useSecondaryRelation) {
    matureCast.relationName = useSecondaryRelation ? cast.secondaryRelationName : cast.relationName;
    matureCast.relationIntro = useSecondaryRelation ? cast.secondaryRelationIntro : cleanPromptText(`与你关系反复推进的${cast.relationName}`);
  }
  return matureCast;
}

