function hashString(text) {
  let hash = 2166136261;
  for (let index = 0; index < text.length; index += 1) {
    hash ^= text.charCodeAt(index);
    hash = Math.imul(hash, 16777619);
  }
  return hash >>> 0;
}

function pickByHash(items, seed, offset = 0) {
  return items[(hashString(`${seed}:${offset}`) || 0) % items.length];
}

