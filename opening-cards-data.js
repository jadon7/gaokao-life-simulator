function normalizeMajorText(value) {
  return String(value || "")
    .toLowerCase()
    .replace(/[\s/／·・|｜-]+/g, "")
    .replace(/[（）()，,。；;：:]/g, "");
}

function profileMajorText(profile = {}) {
  return [profile.majorLabel, profile.major]
    .filter(Boolean)
    .join(" ");
}

function pickDeck(profile = {}) {
  return /女/.test(String(profile.gender || "")) ? femaleOpeningCards : maleOpeningCards;
}

function findCardByMajor(deck, majorName) {
  return deck.find(card => card.major === majorName) || null;
}

function selectOpeningCard(profile = {}) {
  const deck = pickDeck(profile);
  const text = profileMajorText(profile);
  const normalizedText = normalizeMajorText(text);

  const exact = [...deck]
    .sort((left, right) => normalizeMajorText(right.major).length - normalizeMajorText(left.major).length)
    .find(card => normalizedText.includes(normalizeMajorText(card.major)));
  if (exact) return exact;

  for (const [broadKey, majorName] of broadMajorMap) {
    if (normalizedText.includes(broadKey)) return findCardByMajor(deck, majorName) || deck[0];
  }

  for (const [pattern, majorName] of majorRules) {
    if (pattern.test(normalizedText)) return findCardByMajor(deck, majorName) || deck[0];
  }

  const categoryHit = deck.find(card => normalizedText && normalizeMajorText(card.category).includes(normalizedText));
  return categoryHit || deck[0];
}

function randomItem(items) {
  return items[Math.floor(Math.random() * items.length)] || items[0];
}

function randomReplacementName(pool, sourceName) {
  const candidates = pool.filter(name => name !== sourceName);
  return randomItem(candidates.length ? candidates : pool);
}

function relationNameHit(card) {
  const text = JSON.stringify(card);
  const female = relationNamePools.female.find(name => text.includes(name));
  if (female) return { sourceName: female, gender: "女生", pool: relationNamePools.female };
  const male = relationNamePools.male.find(name => text.includes(name));
  if (male) return { sourceName: male, gender: "男生", pool: relationNamePools.male };
  return null;
}

function withRandomRelationName(sourceCard) {
  const hit = relationNameHit(sourceCard);
  if (!hit) return { card: sourceCard, relationName: "", relationGender: "", relationIntro: "" };
  const relationName = randomReplacementName(hit.pool, hit.sourceName);
  const card = JSON.parse(JSON.stringify(sourceCard));
  const replaceName = value => sanitizeSupportRoles(String(value || "").replaceAll(hit.sourceName, relationName));
  card.relationshipTrack = replaceName(card.relationshipTrack);
  card.lifeTrack = replaceName(card.lifeTrack);
  card.summary = replaceName(card.summary);
  if (card.scene) {
    card.scene.title = replaceName(card.scene.title);
    card.scene.body = replaceName(card.scene.body);
  }
  for (const key of ["a", "b"]) {
    if (!card[key]) continue;
    card[key].title = replaceName(card[key].title);
    card[key].desc = replaceName(card[key].desc);
    card[key].tag = replaceName(card[key].tag);
    card[key].consequence = replaceName(card[key].consequence);
  }
  return { card, relationName, relationGender: hit.gender, relationIntro: relationIntroFromCard(card, relationName) };
}

function sanitizeSupportRoles(value) {
  return String(value || "")
    .replace(/赵老师|黄老师|王老师|林老师/g, "老师")
    .replace(/附小老师/g, "附小老师")
    .replace(/电工李工/g, "电工师傅")
    .replace(/带队李工|李工|孙工|陈工/g, "工程师")
    .replace(/系主任/g, "系里老师");
}

function relationIntroFromCard(card, relationName) {
  return relationName ? `第一年有过相处的同学${relationName}` : "";
}

function mainRiasecType(choice = {}) {
  const tag = String(choice.tag || "");
  if (/动手/.test(tag)) return "R";
  if (/查证/.test(tag)) return "I";
  if (/表达|产出/.test(tag)) return "A";
  if (/沟通/.test(tag)) return "S";
  if (/守组/.test(tag)) return "C";
  if (/上场|露脸/.test(tag)) return "E";

  const text = [choice.tag, choice.title, choice.desc].filter(Boolean).join(" ");
  if (/动手|修|做|校准|重烧|下田/.test(text)) return "R";
  if (/查证|核对|复核|推完|整理|证明|报告|剂量|曲线|表格|证据/.test(text)) return "I";
  if (/表达|产出|写|评图|展示|介绍|试讲|讲解|陈述|字幕|展板|结尾/.test(text)) return "A";
  if (/沟通|安抚|被试|提纲/.test(text)) return "S";
  if (/守组|留下|补完|梳理|预演|合奏|照看|定下/.test(text)) return "C";
  return "E";
}

function riasecForChoice(choice) {
  return { ...zeroRiasec, [mainRiasecType(choice)]: 4 };
}

function normalizeChoice(choice = {}) {
  const title = String(choice.title || "").trim();
  const desc = String(choice.desc || "").trim();
  const tag = String(choice.tag || "").trim();
  const consequence = String(choice.consequence || "").trim();
  return {
    title,
    desc,
    tag,
    consequence,
    label: desc ? title + "，" + desc : title,
    riasec: riasecForChoice(choice)
  };
}

export function buildOpeningCard(profile = {}, totalYears = 18) {
  const selected = selectOpeningCard(profile);
  const { card, relationName, relationGender, relationIntro } = withRandomRelationName(selected);
  return {
    summary: "",
    question: "第 1 年 / " + totalYears,
    year: 1,
    phase: "专业入场",
    mainTrack: "life",
    lifeTrack: card.lifeTrack,
    relationshipTrack: card.relationshipTrack,
    callbackSeeds: [card.major, card.category, card.eventType].filter(Boolean),
    openingPreset: true,
    openingMajor: card.major,
    openingCategory: card.category,
    openingRelationName: relationName,
    openingRelationGender: relationGender,
    openingRelationIntro: relationIntro,
    scene: {
      title: card.scene.title,
      body: card.scene.body
    },
    a: normalizeChoice(card.a),
    b: normalizeChoice(card.b)
  };
}
